def get_text(key_path, lang="ja"):
    """Get translated text by key path"""
    keys = key_path.split(".")
    try:
        value = translations[lang]
        for k in keys:
            value = value[k]
        return value
    except KeyError:
        # Fallback to Japanese if key not found
        try:
            value = translations["ja"]
            for k in keys:
                value = value[k]
            return value
        except KeyError:
            return key_path  # Return key path as fallback
